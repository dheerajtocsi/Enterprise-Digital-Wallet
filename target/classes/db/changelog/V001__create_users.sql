CREATE TABLE WALLET_USERS (
    USER_ID            VARCHAR(36)    NOT NULL,
    USERNAME           VARCHAR(50)    NOT NULL,
    EMAIL              VARCHAR(150)   NOT NULL,
    PASSWORD_HASH      VARCHAR(255)   NOT NULL,
    PHONE_ENCRYPTED    VARCHAR(500),
    GOV_ID_ENCRYPTED   VARCHAR(500),
    FULL_NAME          VARCHAR(200),
    ROLE               VARCHAR(20)    NOT NULL DEFAULT 'ROLE_USER',
    IS_ACTIVE          SMALLINT       NOT NULL DEFAULT 1,
    IS_EMAIL_VERIFIED  SMALLINT       NOT NULL DEFAULT 0,
    FAILED_LOGIN_ATTEMPTS SMALLINT     NOT NULL DEFAULT 0,
    LOCKED_UNTIL       TIMESTAMP,
    LAST_LOGIN_AT      TIMESTAMP,
    CREATED_AT         TIMESTAMP      NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT         TIMESTAMP      NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT PK_WALLET_USERS PRIMARY KEY (USER_ID),
    CONSTRAINT UQ_USER_EMAIL UNIQUE (EMAIL),
    CONSTRAINT UQ_USER_USERNAME UNIQUE (USERNAME)
);

COMMENT ON TABLE WALLET_USERS IS 'Enterprise Digital Wallet — User accounts';
COMMENT ON COLUMN WALLET_USERS.PHONE_ENCRYPTED IS 'AES-256-GCM encrypted phone number (PCI-DSS)';
COMMENT ON COLUMN WALLET_USERS.GOV_ID_ENCRYPTED IS 'AES-256-GCM encrypted government ID (PCI-DSS)';
