CREATE TABLE WALLETS (
    WALLET_ID       VARCHAR(36)    NOT NULL,
    WALLET_ADDRESS  VARCHAR(30)    NOT NULL,
    USER_ID         VARCHAR(36)    NOT NULL,
    CURRENCY        VARCHAR(10)    NOT NULL,
    BALANCE         NUMERIC(19,4)  NOT NULL DEFAULT 0,
    LOCKED_BALANCE  NUMERIC(19,4)  NOT NULL DEFAULT 0,
    STATUS          VARCHAR(20)    NOT NULL DEFAULT 'ACTIVE',
    WALLET_NAME     VARCHAR(100),
    DAILY_LIMIT     NUMERIC(19,4)  DEFAULT 100000.00,
    DAILY_SPENT     NUMERIC(19,4)  DEFAULT 0,
    DAILY_RESET_AT  TIMESTAMP,
    VERSION         BIGINT         NOT NULL DEFAULT 0,
    CREATED_AT      TIMESTAMP      NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT      TIMESTAMP      NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT PK_WALLETS PRIMARY KEY (WALLET_ID),
    CONSTRAINT UQ_WALLET_ADDRESS UNIQUE (WALLET_ADDRESS),
    CONSTRAINT FK_WALLET_USER FOREIGN KEY (USER_ID) REFERENCES WALLET_USERS(USER_ID),
    CONSTRAINT CHK_WALLET_BALANCE CHECK (BALANCE >= 0),
    CONSTRAINT CHK_WALLET_LOCKED CHECK (LOCKED_BALANCE >= 0)
);

COMMENT ON TABLE WALLETS IS 'Digital wallet accounts';
COMMENT ON COLUMN WALLETS.VERSION IS 'Optimistic locking version counter';
COMMENT ON COLUMN WALLETS.BALANCE IS 'Total balance including locked amount';
