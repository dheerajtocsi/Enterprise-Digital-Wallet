CREATE TABLE TRANSACTIONS (
    TRANSACTION_ID          VARCHAR(36)    NOT NULL,
    IDEMPOTENCY_KEY         VARCHAR(100)   NOT NULL,
    WALLET_ID               VARCHAR(36)    NOT NULL,
    COUNTERPART_WALLET_ID   VARCHAR(36),
    TYPE                    VARCHAR(20)    NOT NULL,
    STATUS                  VARCHAR(20)    NOT NULL DEFAULT 'PENDING',
    AMOUNT                  NUMERIC(19,4)  NOT NULL,
    FEE                     NUMERIC(19,4)  DEFAULT 0,
    CURRENCY                VARCHAR(10)    NOT NULL,
    BALANCE_BEFORE          NUMERIC(19,4),
    BALANCE_AFTER           NUMERIC(19,4),
    DESCRIPTION             VARCHAR(500),
    REFERENCE_ID            VARCHAR(100),
    METADATA                VARCHAR(2000),
    IP_ADDRESS              VARCHAR(45),
    FAILURE_REASON          VARCHAR(500),
    CREATED_AT              TIMESTAMP      NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT              TIMESTAMP      NOT NULL DEFAULT CURRENT_TIMESTAMP,
    COMPLETED_AT            TIMESTAMP,
    CONSTRAINT PK_TRANSACTIONS PRIMARY KEY (TRANSACTION_ID),
    CONSTRAINT UQ_TXN_IDEMPOTENCY UNIQUE (IDEMPOTENCY_KEY),
    CONSTRAINT FK_TXN_WALLET FOREIGN KEY (WALLET_ID) REFERENCES WALLETS(WALLET_ID),
    CONSTRAINT CHK_TXN_AMOUNT CHECK (AMOUNT > 0)
);

COMMENT ON TABLE TRANSACTIONS IS 'Financial transaction records';
COMMENT ON COLUMN TRANSACTIONS.IDEMPOTENCY_KEY IS 'Prevents duplicate transactions on retry';
